# Введення
n = int(input("Введіть чотирицифрове число: "))

# Виділяємо цифри
a = n // 1000
b = (n // 100) % 10
c = (n // 10) % 10
d = n % 10

# Формуємо нове число після перестановки
new_num = c * 1000 + d * 100 + a * 10 + b


squares = [a**2, b**2, c**2, d**2]

# Вивід
print(f"Нове число: {new_num}")
print(f"Квадрати цифр: {squares}")
